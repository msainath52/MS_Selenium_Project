package ms_conditional_Statements;

public class msswitch_case {

	public static void main(String[] args) {
String x="C";
		
		switch(x) 
		{
		
		case "A":
			
				System.out.println("Block A Code");
				
		        break;
		        
		case "B":
			
			    System.out.println("Block B Code");
			
		        break;
		     
		default:
			
			    System.out.println("Else Block");
			
		        break;
		}		
				

	}

}
